public class Aluno {

    // Atributos
    private String nome;
    private double media;

    // Get e Set
    public String getNome(){
        return nome;
    }
    public void setNome(String nome){
        this.nome = nome;
    }

    public double getMedia(){
        return media;
    }
    public void setMedia(double media){
        this.media = media;
    }
    
}
